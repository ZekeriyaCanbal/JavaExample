package day18_nestedForLoop;

public class C03_WhileLoop {

	public static void main(String[] args) {
		// 20den 30a kadar olan sayýlarý (20 ve 30 dahil)
		//while loop ile yazýn
		
		
		// for loop nasýl kullanýldýðý
		for (int i = 20; i <=30; i++) {
			System.out.print(i+"-");
		}

		System.out.println();
		
		// while loop nasýl kullanýldýðý
		int sayi=20;
		while (sayi<=30) {
			System.out.print(sayi+"-");
			sayi++;
		}

		
		
	}

}
