package day03_Variables;

public class C02_SwapVariables {

	public static void main(String[] args) {
	
		
		int sayi1=10;
		int sayi2=20;
		int bos;
		
		System.out.println("Sayý 1 :"+sayi1+" ve Sayý 2 :"+sayi2);

		bos=sayi2;
		sayi2=sayi1;
		sayi1=bos;
		System.out.println("Sayý 1 :"+sayi1+" ve Sayý 2 :"+sayi2);

		
		
	}

}
